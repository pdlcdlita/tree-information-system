Wine and vinegar are also made from the fruit. It has a high source in vitamin A and vitamin C.

The 1889 book 'The Useful Native Plants of Australia records that the plant was referred to as "Durobbi" by Indigenous Australians, and that "The fruit is much eaten by the natives of India; in appearance it resembles a damson, has a harsh but sweetish flavour, somewhat astringent and acid. It is much eaten by birds, and is a favourite food of the large bat or flying fox. (Brandis)."</td>
