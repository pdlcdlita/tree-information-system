			<td>Moringa oleifera is a fast-growing, deciduous tree that can reach a height of 10–12 m (32–40 ft) and trunk diameter of 45 cm (1.5 ft). The bark has a whitish-grey colour and is surrounded by thick cork. Young shoots have purplish or greenish-white, hairy bark. The tree has an open crown of drooping, fragile branches and the leaves build up a feathery foliage of tripinnate leaves.

The flowers are fragrant and hermaphroditic, surrounded by five unequal, thinly veined, yellowish-white petals. The flowers are about 1.0–1.5 cm long and 2.0 cm broad. They grow on slender, hairy stalks in spreading or drooping flower clusters which have a length of 10–25 cm.

Flowering begins within the first six months after planting. In seasonally cool regions, flowering only occurs once a year between April and June. In more constant seasonal temperatures and with constant rainfall, flowering can happen twice or even all year-round.

The fruit is a hanging, three-sided brown capsule of 20–45 cm size which holds dark brown, globular seeds with a diameter around 1 cm. The seeds have three whitish papery wings and are dispersed by wind and water.</td>
